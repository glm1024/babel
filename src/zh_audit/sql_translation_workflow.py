def _split_simple_csv(text):
    parts = []
    token = []
    in_backtick = False
    for char in text:
        if char == "`":
            in_backtick = not in_backtick
            token.append(char)
            continue
        if char == "," and not in_backtick:
            parts.append("".join(token))
            token = []
            continue
        token.append(char)
    parts.append("".join(token))
    return parts


