def _natural_sort_key(value):
    parts = re.findall(r"\d+|[A-Za-z]+|[^A-Za-z\d]+", str(value or ""))
    key = []
    for part in parts:
        if part.isdigit():
            key.append((0, int(part)))
        else:
            key.append((1, part.lower()))
    return tuple(key)


def _split_sql_csv(text):
    parts = []
    token = []
    in_string = False
    depth = 0
    index = 0
    length = len(text)
    while index < length:
        char = text[index]
        next_char = text[index + 1] if index + 1 < length else ""
        if in_string:
            token.append(char)
            if char == "'" and next_char == "'":
                token.append(next_char)
                index += 2
                continue
            if char == "'":
                in_string = False
            index += 1
            continue
        if char == "'":
            in_string = True
            token.append(char)
            index += 1
            continue
        if char == "(":
            depth += 1
            token.append(char)
            index += 1
            continue
        if char == ")":
            if depth > 0:
                depth -= 1
            token.append(char)
            index += 1
            continue
        if char == "," and depth == 0:
            parts.append("".join(token))
            token = []
            index += 1
            continue
        token.append(char)
        index += 1
    parts.append("".join(token))
    return parts


def _normalize_identifier(value):
    text = str(value or "").strip()
    if text.startswith("`") and text.endswith("`"):
        text = text[1:-1]
    return text.replace("`", "").replace(" ", "").lower()


