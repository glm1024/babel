SQL_TRANSLATION_SESSION_VERSION = 1
SQL_TRANSLATION_OUTPUT_PREFIX = "-- Generated by zh-audit SQL校译"
SQL_IDENTIFIER_PATTERN = r"(?:`[^`]+`|[A-Za-z_][\w$]*)"
SQL_TABLE_PATTERN = r"(?:`[^`]+`|[A-Za-z_][\w$]*)(?:\s*\.\s*(?:`[^`]+`|[A-Za-z_][\w$]*))?"

_INSERT_TABLE_RE = re.compile(
    r"^\s*(insert|replace)\s+into\s+(?P<table>{})".format(SQL_TABLE_PATTERN),
    re.IGNORECASE | re.DOTALL,
)
_INSERT_VALUES_RE = re.compile(
    r"^\s*insert\s+into\s+(?P<table>{})\s*(?P<columns>\([^)]*\))?\s*values\b(?P<values>.*)$".format(
        SQL_TABLE_PATTERN
    ),
    re.IGNORECASE | re.DOTALL,
)
_CREATE_TABLE_RE = re.compile(
    r"^\s*create\s+table\s+(?:if\s+not\s+exists\s+)?(?P<table>{})".format(SQL_TABLE_PATTERN),
    re.IGNORECASE | re.DOTALL,
)
_ALTER_TABLE_RE = re.compile(
    r"^\s*alter\s+table\s+(?P<table>{})\s+(?P<body>.*)$".format(SQL_TABLE_PATTERN),
    re.IGNORECASE | re.DOTALL,
)


