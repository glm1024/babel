def _load_written_item_ids(path):
    text = Path(path).read_text(encoding="utf-8")
    written = set()
    for line in text.splitlines():
        if line.startswith("-- item:"):
            written.add(line[len("-- item:"):].strip())
    return written


def _mask_sql_comments(text):
    chars = []
    index = 0
    length = len(text)
    in_string = False
    in_line_comment = False
    in_block_comment = False
    while index < length:
        char = text[index]
        next_char = text[index + 1] if index + 1 < length else ""
        if in_line_comment:
            if char == "\n":
                in_line_comment = False
                chars.append(char)
            else:
                chars.append(" ")
            index += 1
            continue
        if in_block_comment:
            if char == "*" and next_char == "/":
                chars.append(" ")
                chars.append(" ")
                index += 2
                in_block_comment = False
                continue
            if char == "\n":
                chars.append(char)
            else:
                chars.append(" ")
            index += 1
            continue
        if in_string:
            chars.append(char)
            if char == "'" and next_char == "'":
                chars.append(next_char)
                index += 2
                continue
            if char == "'":
                in_string = False
            index += 1
            continue
        if char == "'" and not in_string:
            in_string = True
            chars.append(char)
            index += 1
            continue
        if char == "-" and next_char == "-":
            in_line_comment = True
            chars.append(" ")
            chars.append(" ")
            index += 2
            continue
        if char == "#":
            in_line_comment = True
            chars.append(" ")
            index += 1
            continue
        if char == "/" and next_char == "*":
            in_block_comment = True
            chars.append(" ")
            chars.append(" ")
            index += 2
            continue
        chars.append(char)
        index += 1
    return "".join(chars)


def _split_sql_statements(text, masked_text):
    statements = []
    start_index = None
    start_line = 1
    line = 1
    in_string = False
    index = 0
    length = len(masked_text)
    while index < length:
        char = masked_text[index]
        next_char = masked_text[index + 1] if index + 1 < length else ""
        if start_index is None and not char.isspace():
            start_index = index
            start_line = line
        if in_string:
            if char == "'" and next_char == "'":
                index += 2
                continue
            if char == "'":
                in_string = False
        else:
            if char == "'":
                in_string = True
            elif char == ";":
                if start_index is not None:
                    statements.append(
                        {
                            "text": text[start_index:index],
                            "masked_text": masked_text[start_index:index],
                            "line": start_line,
                        }
                    )
                    start_index = None
        if char == "\n":
            line += 1
        index += 1
    if start_index is not None:
        statements.append(
            {
                "text": text[start_index:],
                "masked_text": masked_text[start_index:],
                "line": start_line,
            }
        )
    return statements


def _parse_identifier_list(columns_text):
    inner = str(columns_text or "").strip()
    if inner.startswith("(") and inner.endswith(")"):
        inner = inner[1:-1]
    parts = _split_simple_csv(inner)
    result = []
    for part in parts:
        raw = part.strip()
        if not raw:
            continue
        result.append(
            {
                "raw": raw,
                "normalized": _normalize_identifier(raw),
            }
        )
    return result


def _parse_values_groups(values_text, base_line):
    groups = []
    in_string = False
    depth = 0
    start = None
    line = base_line
    group_line = base_line
    index = 0
    length = len(values_text)
    while index < length:
        char = values_text[index]
        next_char = values_text[index + 1] if index + 1 < length else ""
        if in_string:
            if char == "'" and next_char == "'":
                index += 2
                continue
            if char == "'":
                in_string = False
        else:
            if char == "'":
                in_string = True
            elif char == "(":
                if depth == 0:
                    start = index
                    group_line = line
                depth += 1
            elif char == ")":
                if depth > 0:
                    depth -= 1
                    if depth == 0 and start is not None:
                        groups.append(
                            {
                                "text": values_text[start:index + 1],
                                "line": group_line,
                            }
                        )
                        start = None
        if char == "\n":
            line += 1
        index += 1
    return groups


def _split_tuple_values(group_text):
    inner = str(group_text or "").strip()
    if inner.startswith("(") and inner.endswith(")"):
        inner = inner[1:-1]
    return _split_sql_csv(inner)


def _split_simple_csv(text):
    parts = []
    token = []
    in_backtick = False
    for char in text:
        if char == "`":
            in_backtick = not in_backtick
            token.append(char)
            continue
        if char == "," and not in_backtick:
            parts.append("".join(token))
            token = []
            continue
        token.append(char)
    parts.append("".join(token))
    return parts


def _split_sql_csv(text):
    parts = []
    token = []
    in_string = False
    depth = 0
    index = 0
    length = len(text)
    while index < length:
        char = text[index]
        next_char = text[index + 1] if index + 1 < length else ""
        if in_string:
            token.append(char)
            if char == "'" and next_char == "'":
                token.append(next_char)
                index += 2
                continue
            if char == "'":
                in_string = False
            index += 1
            continue
        if char == "'":
            in_string = True
            token.append(char)
            index += 1
            continue
        if char == "(":
            depth += 1
            token.append(char)
            index += 1
            continue
        if char == ")":
            if depth > 0:
                depth -= 1
            token.append(char)
            index += 1
            continue
        if char == "," and depth == 0:
            parts.append("".join(token))
            token = []
            index += 1
            continue
        token.append(char)
        index += 1
    parts.append("".join(token))
    return parts


def _normalize_identifier(value):
    text = str(value or "").strip()
    if text.startswith("`") and text.endswith("`"):
        text = text[1:-1]
    return text.replace("`", "").replace(" ", "").lower()


def _table_leaf_name(table_expression):
    text = str(table_expression or "").strip()
    if "." not in text:
        return text
    return text.split(".")[-1]


def _sql_value_to_text(token):
    value = str(token or "").strip()
    if not value:
        return ""
    if value.upper() == "NULL":
        return ""
    if len(value) >= 2 and value[0] == "'" and value[-1] == "'":
        return value[1:-1].replace("''", "'")
    return value


def _sql_value_to_display(token):
    value = str(token or "").strip()
    if not value:
        return ""
    if len(value) >= 2 and value[0] == "'" and value[-1] == "'":
        return value[1:-1].replace("''", "'")
    return value


def _escape_sql_string(value):
    return str(value or "").replace("'", "''")


def _safe_file_name_component(value):
    chars = []
    for char in str(value or ""):
        if char.isalnum() or char in ("-", "_"):
            chars.append(char)
        else:
            chars.append("_")
    result = "".join(chars).strip("_")
    return result or "table"


def _contains_locked_terms(candidate, locked_terms):
    value = str(candidate or "")
    for term in locked_terms:
        if term["target"] not in value:
            return False
    return True


def _timestamp():
    return datetime.now().astimezone().replace(microsecond=0).isoformat()
