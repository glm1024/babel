    def _finalize_item(self, item, should_auto_accept, event_label):
        self.processed += 1
        if should_auto_accept and item.get("can_accept", True):
            self._apply_item(item, "accepted", "已自动审批")
            return
        item["status"] = "pending"
        item["updated_at"] = _timestamp()
        self.pending_ids.append(item["id"])
        self.pending += 1
        if item.get("validation_state") == "failed":
            self.failed += 1
        self._push_event(self._pending_event_label(item, event_label), item, item.get("candidate_text", ""))
        self._persist_locked()

    def _validated_candidate_text(self, item, candidate_text=None):
        value = sanitize_candidate_text(
            item.get("candidate_text", "") if candidate_text is None else candidate_text
        )
        if not value:
            raise ValueError("Candidate translation is empty for SQL item {}".format(item.get("primary_key_display", "")))
        if not has_matching_placeholders(item.get("source_text", ""), value):
            raise ValueError(
                "Candidate translation lost placeholders for SQL item {}".format(item.get("primary_key_display", ""))
            )
        return value

    def _apply_item(self, item, status, reason):
        item["candidate_text"] = self._validated_candidate_text(item)
        appended = self._append_update_sql_once(item)
        item["status"] = status
        item["target_text"] = item.get("candidate_text", "")
        item["updated_at"] = _timestamp()
        item["accepted_reason"] = reason
        if item["id"] in self.pending_ids:
            self.pending_ids.remove(item["id"])
            self.pending -= 1
        self.accepted += 1
        if appended:
            self.appended += 1
        self._push_recent(item["id"])
        self._push_event(reason, item, item.get("candidate_text", ""))
        self._persist_locked()

    def _pending_event_label(self, item, default_label):
        if item.get("validation_state") == "failed":
            phase = str(item.get("failure_phase", "") or "本地校验")
            if phase == "模型":
                prefix = "候选生成失败"
            elif phase == "AI复核":
                prefix = "候选通过 AI复核失败"
            else:
                prefix = "候选通过本地校验失败"
            return "{}：{}".format(prefix, item.get("validation_issue") or "请重新生成")
        return default_label

    def _append_update_sql_once(self, item):
        item_id = item["id"]
        if item_id in self.written_item_ids:
            return False
        output_path = Path(self.output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        rendered = "\n".join(
            [
                "-- item:{}".format(item_id),
                "-- source:{}:{}".format(item.get("source_path", ""), item.get("line", 0)),
                _build_update_sql(item),
                "",
            ]
        )
        with output_path.open("a", encoding="utf-8") as handle:
            handle.write(rendered)
        self.written_item_ids.add(item_id)
        return True

    def _initialize_output_file(self):
        output_path = Path(self.output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        header = "\n".join(
            [
                SQL_TRANSLATION_OUTPUT_PREFIX,
                "-- table: {}".format(self.table_name),
                "-- locator_field: {}".format(self.primary_key_field),
                "-- source_field: {}".format(self.source_field),
                "-- target_field: {}".format(self.target_field),
                "-- source_directory: {}".format(self.directory_path),
                "-- created_at: {}".format(_timestamp()),
                "",
            ]
        )
        output_path.write_text(header, encoding="utf-8")
        self.written_item_ids = set()

    def _build_output_path(self):
        directory = Path(self.directory_path)
        safe_table = _safe_file_name_component(self.table_name or "table")
        file_name = "{}_sql_audit_{}.sql".format(safe_table, datetime.now().strftime("%Y%m%d_%H%M%S"))
        return str((directory / file_name).resolve())

    def _push_recent(self, item_id):
        if item_id in self.recent_ids:
            self.recent_ids.remove(item_id)
        self.recent_ids.insert(0, item_id)
        del self.recent_ids[50:]

